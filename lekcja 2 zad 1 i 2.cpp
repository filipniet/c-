#include <iostream>
using namespace std;
int main()
{
    int tab[10][10];
    
    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10; j++) {
            tab[i][j] = 0;
        }
    }
    for (int a = 0; j < 10; j++) {
        for (int b = 0; j < 10; j++) {
            cout << tab[a][b];
        }
        endl;
    }

    
}